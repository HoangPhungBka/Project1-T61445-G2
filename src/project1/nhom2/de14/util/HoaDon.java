
package QuanLyKhachSan;


public class HoaDon {
    private String maHD;
    private String maDatHang;
    private String thoiGianThanhToan;
    private String tienPhong;
    private String tienDichvu;

    public String getMaHD() {
        return maHD;
    }

    public void setMaHD(String maHD) {
        this.maHD = maHD;
    }

    public String getMaDatHang() {
        return maDatHang;
    }

    public void setMaDatHang(String maDatHang) {
        this.maDatHang = maDatHang;
    }

    public String getThoiGianThanhToan() {
        return thoiGianThanhToan;
    }

    public void setThoiGianThanhToan(String thoiGianThanhToan) {
        this.thoiGianThanhToan = thoiGianThanhToan;
    }

    public String getTienPhong() {
        return tienPhong;
    }

    public void setTienPhong(String tienPhong) {
        this.tienPhong = tienPhong;
    }

    public String getTienDichvu() {
        return tienDichvu;
    }

    public void setTienDichvu(String tienDichvu) {
        this.tienDichvu = tienDichvu;
    }
    
}
