package QuanLyKhachSan;

/**
 *
 * @author LeThanhLoi
 */
public class DatPhong {
    private String maDatPhong;
    private String maKH;
    private String thoiGianNhan;
    private String thoiGianTra;
    private int soPhongDat;
    private String  tienDatCoc;
    private String maNV;

    public String getMaDatPhong() {
        return maDatPhong;
    }

    public void setMaDatPhong(String maDatPhong) {
        this.maDatPhong = maDatPhong;
    }

    public String getMaKH() {
        return maKH;
    }

    public void setMaKH(String maKH) {
        this.maKH = maKH;
    }

    public String getThoiGianNhan() {
        return thoiGianNhan;
    }

    public void setThoiGianNhan(String thoiGianNhan) {
        this.thoiGianNhan = thoiGianNhan;
    }

    public String getThoiGianTra() {
        return thoiGianTra;
    }

    public void setThoiGianTra(String thoiGianTra) {
        this.thoiGianTra = thoiGianTra;
    }

    public int getSoPhongDat() {
        return soPhongDat;
    }

    public void setSoPhongDat(int soPhongDat) {
        this.soPhongDat = soPhongDat;
    }

    public String getTienDatCoc() {
        return tienDatCoc;
    }

    public void setTienDatCoc(String tienDatCoc) {
        this.tienDatCoc = tienDatCoc;
    }

    public String getMaNV() {
        return maNV;
    }

    public void setMaNV(String maNV) {
        this.maNV = maNV;
    }
}
